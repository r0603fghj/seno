package course14;

// 整数リテラル（10進／8進／16進）

class Reidai {

	public static void main(String[] args) {
		int a = 100; // 10進数の13
		int b = 0b01100100; //  8進数の13
		int c = 0x64; // 16進数の13

		System.out.println("a = " + a);
		System.out.println("b = " + b);
		System.out.println("c = " + c);
	}
}
